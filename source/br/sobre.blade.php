<p>
De tempos em tempos sites sobre tecnologia surgem e desaparecem,
alguns com conteúdos interessantes, outros nem tanto. Mas uma
característica muito comum entre a maioria deles é que os
conteúdos são otimizados para máquinas, em vez de pessoas.
</p>

<p>
O thePHP Website acredita que todos os(as) desenvolvedores(as),
independentemente de seu nível de senioridade, merecem ser
tratados(as) como humanos. Sem contextos subentendidos,
sem arrogância, sem egos inflados.
</p>

<p>
O primeiro autor do site, <a href="https://twitter.com/nawarian" rel="nofollow">@nawarian</a>,
é um brasileiro nascido em Mogi das Cruzes, SP. Que muito cedo em
sua carreira participou da comunidade
<a href="https://phpsp.org.br/" rel="nofollow">PHPSP</a>
onde teve a oportunidade de entender como a comunidade PHP funciona.
</p>

<p>
<strong>Este site é para a comunidade</strong>,
<a href="https://github.com/nawarian/The-PHP-Website" rel="nofollow">é aberto</a>
e é simples na aparência. Com a maior intenção de oferecer o mais
rico conteúdo possível com o máximo respeito por todos(as) usuários(as).
</p>

<h2>Os Valores do thePHP Website</h2>

<p>
Desde seu início, para sempre, o thePHP Website trará consigo,
em ordem de importancia, os valores:
</p>

<ol>
  <li>Respeito</li>
  <li>Ser objetivo</li>
  <li>Ser aberto</li>
</ol>

<h3>1. Respeito</h3>
<p>
O principal valor que o thePHP Website carrega consigo é o
<strong>respeito pelos(as) usuários(as)</strong>. Isto se traduz desde o
conteúdo até o design e layout.
</p>

<p>
O respeito pela leitura, pelos recursos consumidos pelos(as)
leitores(as) como dados, bateria e esforço ocular.
</p>

<p>
Como bons(as) engenheiros(as) todos(as) queremos um conteúdo que
agregue valor muito mais do que uma interface chamativa e cheia de
coisas inúteis que só oneram e desrespeitam o nosso tempo e recursos.
</p>

<p>
Também não queremos intrigas, meias verdades ou dados sem fontes.
Queremos opiniões embasadas, textos informativos com fontes e
provas. <strong>Queremos exemplos de código que remetem ao dia a dia
profissional</strong>, nada de <code>$cachorro->come($arroz)</code> e
coisas que só fazem sentido num livro infantil.
</p>

<h3>2. Ser objetivo</h3>
<p>
Toda publicação que busca responder a uma questão ou tópico assim
o fará. Sem trazer histórias de 10 mil anos atrás, relacionar com
textos que não tenham a menor conexão, a não ser que seja realmente
necessário.
</p>

<p>
<strong>Este SEO barato</strong> de repetição de palavras chave, criação de links
falsos e repetição de palavras/citações que não são nada relevantes
ao conteúdo <strong>aqui não será aceito.</strong>
</p>

<p>
Todo(a) leitor(a) pode sentir-se livre para reclamar que determinado
conteúdo possui informações desnecessárias ou introduções muito longas.
Nós não nos importamos se o(a) leitor(a) passa 20 segundos em nossas
páginas, desde que estes 20 segundos tenham sido suficientes para
responder a determinada questão ou trazer determinado insight.
</p>

<h3>3. Ser aberto</h3>
<p>
O thePHP Website nasceu aberto e assim permanecerá. Aceitará conteúdo
de quem desejar, desde que esteja alinhado com os valores do website.
</p>

<p>
Todas decisões, números, estatísticas e planos podem ser perguntados
por qualquer pessoa a qualquer momento.
</p>

<p>
<strong>Mas não se engane!</strong> O thePHP Website é proprietário e
comercial. Mesmo que a intenção seja de retornar os dividendos para a
comunidade na forma de conteúdo e serviços, ainda há uma equipe
física recebendo por ele.
</p>

<p>
Portanto espere encontrar anúncios, propagandas ou links patrocinados
de tempos em tempos. Mas pode ter certeza de que SEMPRE serão relevantes
ao conteúdo em questão.
</p>

<h2>O thePHP Website possui uma missão</h2>

<p>
A missão do thePHP Website é aquecer novamente a comunidade PHP e
trazer conteúdo de nível profissional aos seus consumidores(as).
</p>

<p>
Nós entendemos que a profissão programador é uma das melhores
formas de auxiliar a mobilidade social de indivíduos e possui
enorme potencial em países em desenvolvimento, como o Brasil.
E que a produção de conteúdo, serviços e cursos que a auxiliem
são essenciais e devem ser levados em consideração a cada vírgula
escrita.
</p>

<p>
O thePHP Website ainda é jovem, mas traz em seu coração uma vontade
gigante e explícita de entregar e produzir cada vez mais conteúdos
e serviços de qualidade.
</p>

<p>
O seu conteúdo aqui será sempre aceito e reconhecido. Alterações
de código também. Tudo que possa fazer a gente crescer juntos,
como uma comunidade.
</p>

